package Study1_5;

public class Student {
	String name;
	int age;
	
	public Student(String n, int a) {
		name = n;
		age = a;
		
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
